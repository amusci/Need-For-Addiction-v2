    public RpcFrame(String appId) {
        lib = DiscordRPC.INSTANCE;
        this.applicationId = appId;
        initComponents();
        this.setLocationRelativeTo(null);
        jspnStart.setValue((int)(System.currentTimeMillis() / 1000));
        executor = Executors.newSingleThreadScheduledExecutor();
        executor.submit(this::initRPC);
    }
    
    public void initRPC() {
        DiscordEventHandlers handlers = new DiscordEventHandlers();
        handlers.ready = (user) -> this.registerUser(user);
        handlers.errored = (errorCode, message) -> jlblStatus.setText("Error #" + errorCode + ": " + message);
        handlers.disconnected = (errorCode, message) -> jlblStatus.setText("Disconnected: " + message);
        lib.Discord_Initialize(applicationId, handlers, true, steamId);
        executor.schedule(lib::Discord_RunCallbacks, 2, TimeUnit.SECONDS);
    }
    
    public void registerUser(DiscordUser user) {
        this.user = user;
        jlblStatus.setText("Connected - " + user.username + "#" + user.discriminator);
    }
    
    public void updatePresence(DiscordRichPresence presence) {
        lib.Discord_UpdatePresence(presence);
        jlblStatus.setText("Presence updated - " + user.username + "#" + user.discriminator);
    }

