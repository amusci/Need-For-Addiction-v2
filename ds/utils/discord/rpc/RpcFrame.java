    private void jbtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUpdateActionPerformed
        DiscordRichPresence presence = new DiscordRichPresence();
        String str = jtxtState.getText();
        if (str != null && !str.isEmpty()) presence.state = str;
        str = jtxtDetails.getText();
        if (str != null && !str.isEmpty()) presence.details = str;
        int val = (int)jspnStart.getValue();
        if (val >= 0) presence.startTimestamp = val;
        val = (int)jspnEnd.getValue();
        if (val >= 0) presence.endTimestamp = val;
        str = jtxtLargeImgKey.getText();
        if (str != null && !str.isEmpty()) presence.largeImageKey = str;
        str = jtxtLargeImgText.getText();
        if (str != null && !str.isEmpty()) presence.largeImageText = str;
        str = jtxtSmallImgKey.getText();
        if (str != null && !str.isEmpty()) presence.smallImageKey = str;
        str = jtxtSmallImgText.getText();
        if (str != null && !str.isEmpty()) presence.smallImageText = str;
        str = jtxtPartyID.getText();
        if (str != null && !str.isEmpty()) presence.partyId = str;
        val = (int)jspnPartySize.getValue();
        if (val >= 0) presence.partySize = val;
        val = (int)jspnPartyMax.getValue();
        if (val >= 0) presence.partyMax = val;
        str = jtxtSpectate.getText();
        if (str != null && !str.isEmpty()) presence.spectateSecret = str;
        str = jtxtJoin.getText();
        if (str != null && !str.isEmpty()) presence.joinSecret = str;
        executor.submit(() -> this.updatePresence(presence));
    }//GEN-LAST:event_jbtnUpdateActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        lib.Discord_ClearPresence();
        lib.Discord_Shutdown();
        executor.shutdown();
        try {
            executor.awaitTermination(10, TimeUnit.SECONDS);
        } catch (InterruptedException ex) {}
        this.dispose();
    }//GEN-LAST:event_formWindowClosing

